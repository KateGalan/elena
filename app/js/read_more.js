$(function ()
	  {  var h = 50, t = $('.ta1'), min= t[0].scrollHeight, max = 10;
	   $('.green1').on('click', function (event)
	  {
	   var H = t.height();
	   if(H == min){H = max}
	   else if (H  + h > max){H = min}
	   else {H += h};
	   t.height(H);
	   $(this).text(H == min ? 'Hide' : 'Read more')
	   return false
	  })
	  })
